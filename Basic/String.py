"""Declared a string in a variable"""
name = "habibur rahman"

"""Changing Case in a String without change value"""
print(name.title())
print(name + "\n")

"""Convert Upper and Lower Case"""
print(name.upper())
print(name.lower() + "\n")

"""Concat string"""
first_name = "habibur"
last_name = "rahman"
print(first_name + " " + last_name)
print("Hello, " + name.title() + "!\n")
